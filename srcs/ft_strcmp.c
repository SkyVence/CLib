int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (1)
	{
		if (s1[i] == '\0' && s2[i] == '\0')
			break ;
		if (s1[i] != s2[i])
		{
			if (s1[i] < s2[i])
				return (-1);
			if (s1[i] > s2[i])
				return (1);
			break ;
		}
		i++;
	}
	return (0);
}

